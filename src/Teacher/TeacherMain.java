    private void jTextField4CaretUpdate(javax.swing.event.CaretEvent evt) {//GEN-FIRST:event_jTextField4CaretUpdate
        String str = jTextField4.getText();
        double marks=0;
        if(!str.equals(" "))
            marks = Double.parseDouble(str);
        else
            updateMarks.setEnabled(false);
        if(str.length()>0)
        {
            updateMarks.setEnabled(true);
        }
        else
        {
            updateMarks.setEnabled(false);
        }
        if(marks>=0 && marks <=100)
        {
            updateMarks.setEnabled(true);
        }
        else
        {
            updateMarks.setEnabled(false);
        }
    }//GEN-LAST:event_jTextField4CaretUpdate

