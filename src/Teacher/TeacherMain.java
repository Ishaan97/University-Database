    private void changePwdActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_changePwdActionPerformed
        try
        {
            DbConnect.Connect();

            String query1 = "Select UserId from Teacher where TID = "+jTextField1.getText()+";";
            char[] newPwd = jPasswordField1.getPassword();
            char[] confPwd = jPasswordField2.getPassword();

            if (newPwd==confPwd)
            {
                ResultSet rs = DbConnect.stmt.executeQuery(query1);
                if (rs.next())
                {
                    String query = "Update AdminTable set Password = '"+jPasswordField1.getPassword()+"' where UserId = "+rs.getString(1)+";";
                    DbConnect.stmt.executeUpdate(query);
                }
                JOptionPane.showMessageDialog(this,"Password Changed Successfuly");
            }
            else
            JOptionPane.showMessageDialog(this,"Confirm Password Not Equal to the New Password. Enter Again!!!");
            jPasswordField1.setText("");
            jPasswordField2.setText("");
            DbConnect.Disconnect();
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(this,e.getMessage());
        }
    }//GEN-LAST:event_changePwdActionPerformed

